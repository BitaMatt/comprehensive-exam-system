# 获取系统语言
def get_system_language():
    import ctypes
    windll = ctypes.windll.kernel32
    lang_code = windll.GetUserDefaultUILanguage()
    lang_id = hex(lang_code)[2:]
    if lang_id.startswith('804'):  # 中文简体
        return 'zh_CN'
    else:  # 默认英文
        return 'en_US'

# 获取翻译
def _(key, lang=None, **kwargs):
    if lang is None:
        lang = get_system_language()
    if lang not in LANGUAGES:
        lang = 'en_US'
    if key in LANGUAGES[lang]:
        return LANGUAGES[lang][key].format(**kwargs)
    return key